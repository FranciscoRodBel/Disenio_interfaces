package com.mycompany.tareapp.modelo.idioma;

/**
 * Clase para el modelo del idioma de la cabecera
 *
 * @author Francisco
 */
public class Cabecera {

    private String tareas;
    private String listas;
    private String idioma;
    private String cuenta;
    private String ajustes;
    private String cerrar_sesion;
    
    public String getTareas() {
        return tareas;
    }

    public void setTareas(String tareas) {
        this.tareas = tareas;
    }

    public String getListas() {
        return listas;
    }

    public void setListas(String listas) {
        this.listas = listas;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public String getCuenta() {
        return cuenta;
    }

    public void setCuenta(String cuenta) {
        this.cuenta = cuenta;
    }

    public String getAjustes() {
        return ajustes;
    }

    public void setAjustes(String ajustes) {
        this.ajustes = ajustes;
    }

    public String getCerrar_sesion() {
        return cerrar_sesion;
    }

    public void setCerrar_sesion(String cerrar_sesion) {
        this.cerrar_sesion = cerrar_sesion;
    }
    
}
